package basicconcepts;

public class datatypes {
	
	public static void main(String[]args)
	{
		System.out.println("datatypes");
		
		byte num=127;
		System.out.println("byte value is: "+num);
		
		short a=12345;
		System.out.println("short value is:"+a);
		
		int b=8282828;
		System.out.println("int value is:"+b);
		
		long c=8282828282828282828l;
		System.out.println("long value is:"+c);
		
		float f=857.7f;
		System.out.println("float value is: "+f);
		
		double d=7567454;
		System.out.println("double value is: "+d);
		
		boolean result=true;
		System.out.println("Boolean Value is: "+result);
		
		char ch='p';
		System.out.println("Character value is: "+ch);
		
		String s="Nikhil Shinde";
		System.out.println("String is: "+s);	
	}
}

